CREATE TABLE `netease_user` (
	`fID` VARCHAR(50) NOT NULL COMMENT 'ID',
	`fType` VARCHAR(50) NULL DEFAULT NULL COMMENT 'QQ;weibo;weixin;sim',
	`fName` VARCHAR(50) NULL DEFAULT NULL COMMENT '姓名',
	`passwd` VARCHAR(50) NULL DEFAULT NULL COMMENT '密码',
	`fPhoneNumber` VARCHAR(20) NULL DEFAULT NULL COMMENT '电话',
	`fAddress` VARCHAR(200) NULL DEFAULT NULL COMMENT '地址',
	PRIMARY KEY (`fID`)
)
COLLATE='utf8_general_ci'
ENGINE=InnoDB
;
