package com.jake.bean;

public class Admin {
	private int id;
	private String webtitle;
	private String adminhttp;
	private String adminname;
	private String adminpass;
	private String adminmail;
	private String webadvice;
	private String adminqq;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getWebtitle() {
		return webtitle;
	}
	public void setWebtitle(String webtitle) {
		this.webtitle = webtitle;
	}
	public String getAdminhttp() {
		return adminhttp;
	}
	public void setAdminhttp(String adminhttp) {
		this.adminhttp = adminhttp;
	}
	public String getAdminname() {
		return adminname;
	}
	public void setAdminname(String adminname) {
		this.adminname = adminname;
	}
	public String getAdminpass() {
		return adminpass;
	}
	public void setAdminpass(String adminpass) {
		this.adminpass = adminpass;
	}
	public String getAdminmail() {
		return adminmail;
	}
	public void setAdminmail(String adminmail) {
		this.adminmail = adminmail;
	}
	public String getWebadvice() {
		return webadvice;
	}
	public void setWebadvice(String webadvice) {
		this.webadvice = webadvice;
	}
	public String getAdminqq() {
		return adminqq;
	}
	public void setAdminqq(String adminqq) {
		this.adminqq = adminqq;
	}
}
